/**
 * 根据得分返回称号
 */
function getTitle(score) {
  if (score >= 90) return '首席人才官';
  if (score >= 75) return '资深HR总监';
  if (score >= 60) return '高级HR经理';
  if (score >= 45) return 'HR专员';
  if (score >= 30) return '实习HR';
  return '前台临时工';
}
